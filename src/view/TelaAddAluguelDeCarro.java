    private void rbluxoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_rbluxoItemStateChanged
         AddAluguelDeCarro();
        if(rbluxo.isSelected()){
            tanquecheio.setEnabled(true);
            seguro.setEnabled(true);
        }else{
            tanquecheio.setEnabled(false);
            seguro.setEnabled(false);
        }
        atualizar();
    }//GEN-LAST:event_rbluxoItemStateChanged

